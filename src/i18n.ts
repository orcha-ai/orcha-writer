export function translateText(language: unknown, zhText: string, params?: TranslateParams): string {
  const template = isEnglishLanguage(language) ? (enTextByZh[zhText] || zhText) : zhText;
  if (!params) return template;
  return template.replace(/\{(\w+)\}/g, (match, key: string) => (
    Object.prototype.hasOwnProperty.call(params, key) ? String(params[key]) : match
  ));
}

export function getDocumentLanguage(): AppLanguage {
  if (typeof document === 'undefined') return 'zh-CN';
  return normalizeAppLanguage(
    document.documentElement.getAttribute('data-locale')
    || document.documentElement.lang,
  );
}

